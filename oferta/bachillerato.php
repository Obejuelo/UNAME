    click++;
    if (click % 2 == 1) {
        posicion.style.background = "#A20000";
        //$('nav .container').removeClass('width');
    } else {
        setTimeout("retardo()", 300);
    }
});

function retardo() {
    posicion.style.background = "rgba(10, 107, 60, .1)";
    $('nav .container').addClass('width');
}

